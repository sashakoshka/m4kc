#pragma once

/* Coords
 * Stores xyz coordinates
 */
typedef struct {
        float x;
        float y;
        float z;
} Coords;

/* IntCoords
 * Stores xyz integer coordinates
 */
typedef struct {
        int x;
        int y;
        int z;
} IntCoords;

/* Vector2D
 * Two dimensional floating point vector
 */
typedef struct {
        float x;
        float y;
} Vector2D;
